function packageLog (name, ...args) {

  if (name.length > 7)
    name = name.slice(0, 5) + '..'

  name = `@benzed/${name}\t`.bold.green

  console.log(name, ...args)
}

/******************************************************************************/
// Exports
/******************************************************************************/

module.exports = packageLog
