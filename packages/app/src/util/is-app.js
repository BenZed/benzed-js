import is from 'is-explicit'

/******************************************************************************/
// Data
/******************************************************************************/

const METHODS = [
  'get', 'set', 'configure', 'use', 'service'
]

/******************************************************************************/
// Main
/******************************************************************************/

function isApp (app) {
  if (this !== undefined)
    app = this

  return is.object(app) && METHODS.every(method => is.func(app[method]))

}

/******************************************************************************/
// Exports
/******************************************************************************/

export default isApp
