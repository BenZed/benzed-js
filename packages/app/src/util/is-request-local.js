function isRequestLocal (req) {

  const { remoteAddress: ip } = req.connection

  return ip === '127.0.0.1' ||
    ip === '::ffff:127.0.0.1' ||
    ip === '::1'

}

/******************************************************************************/
// Exports
/******************************************************************************/

export default isRequestLocal
