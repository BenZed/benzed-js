import is from 'is-explicit'

/******************************************************************************/
// Data
/******************************************************************************/

const METHODS = [
  'find', 'get', 'update', 'patch', 'remove', 'create', 'setup'
]

/******************************************************************************/
// Main
/******************************************************************************/

const isService = input =>
  is.object(input) &&
  METHODS.some(method => is.func(input[method]))

/******************************************************************************/
// Exports
/******************************************************************************/

export default isService
