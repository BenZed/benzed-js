import { hasNumericLength } from './is-array-like'

/******************************************************************************/
// Main
/******************************************************************************/

function last (arr) {

  if (this !== undefined)
    arr = this

  return hasNumericLength(arr)
    ? arr[arr.length - 1]
    : undefined
}

/******************************************************************************/
// Exports
/******************************************************************************/

export default last
