
/******************************************************************************/
// Main
/******************************************************************************/

const jsxToData = (type, props = {}, ...children) => {

  const data = { ...props }

  if (type !== 'app')
    data.type = type

  for (const { type, name, ...child } of children)
    data[type] = child

  return data
}

/******************************************************************************/
// Exports
/******************************************************************************/

export default jsxToData
