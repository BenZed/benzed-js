class Color {

  r = 0
  g = 0
  b = 0
  a = 1

  toString () {

    const { r, g, b, a } = this

    return a < 1
      ? `rgba(${r}, ${g}, ${b}, ${a})`
      : `rgb(${r}, ${g}, ${b})`

  }

}

