
/******************************************************************************/
// Main
/******************************************************************************/

function lerp (from, to, delta) {

  // handles #::lerp(#,#)
  if (typeof this === 'number') {
    delta = to
    to = from
    from = this
  }

  return from + delta * (to - from)

}

/******************************************************************************/
// Exports
/******************************************************************************/

export default lerp
