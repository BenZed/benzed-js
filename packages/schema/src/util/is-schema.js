import is from 'is-explicit'

/******************************************************************************/
// Data
/******************************************************************************/

const $$schema = Symbol('schema-data')

/******************************************************************************/
// Main
/******************************************************************************/

function isSchema (input) {
  if (this !== undefined)
    input = this

  return is.object(input) && is.plainObject(input[$$schema])
}

/******************************************************************************/
// Exports
/******************************************************************************/

export default isSchema

export {
  $$schema
}
